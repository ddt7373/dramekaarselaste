    <h1>Dra Mekaar – Inligting & Kenmerke</h1>
    <p><strong>Dra Mekaar</strong> is die NHKA (Nederduitse Hervormde Kerk van Afrika) se digitale platform vir gemeentes, lidmate en bedienings. Hierdie dokument beskryf al die funksies en kenmerke van die app.</p>

    <h2>1. Paneelbord (Dashboard)</h2>
    <p>Sentrale oorsig met vinnige toegang tot belangrike inligting: kennisgewings, komende gebeurtenisse, wyke, en persoonlike vordering.</p>

    <h2>2. Gemeente & Wyke</h2>
    <h3>My Wyk / Gemeente</h3>
    <p>Bestuur wyke, lidmate en besoekpunte. Predikante sien die hele gemeente; wykleiers sien net hul wyk.</p>
    <h3>Gemeente Kaart</h3>
    <p>Interaktiewe kaart van die gemeente met lidmate en besoekpunte.</p>
    <h3>NHKA Kaart</h3>
    <p>Denominasie-wye kaart van alle NHKA gemeentes in Suid-Afrika.</p>
    <h3>Wyk Toewysing</h3>
    <p>Wys lidmate aan wyke toe en bestuur wykstruktuur.</p>
    <h3>Besoekpunt Toewysing</h3>
    <p>Koppel besoekpunte aan lidmate vir pastoraat en opvolg.</p>

    <h2>3. Kerkadministrasie</h2>
    <h3>Konsistorieboek</h3>
    <p>Digitale konsistorieboek vir kerkraadsnotules, besluitneming en argivering.</p>
    <h3>Erediens Info</h3>
    <p>Bestuur eredienstye, liturgie en Sondag-inligting.</p>
    <h3>Nuusbrief</h3>
    <p>Skep en stuur gemeente-nuusbriewe aan lidmate.</p>
    <h3>Dokumente Bestuur</h3>
    <p>Sentrale stoorplek vir kerkdokumente, registers en argiewe.</p>
    <h3>My Dokumente</h3>
    <p>Persoonlike toegang tot gekas dokumente vir van-lyn-af gebruik.</p>

    <h2>4. Lidmaatskap & Oordrag</h2>
    <h3>Lidmaatskap Oordrag</h3>
    <p>Proses vir lidmate wat na ander gemeentes of kerke oorgaan.</p>
    <h3>My Profiel</h3>
    <p>Bekyk en wysig persoonlike profiel, kontakbesonderhede en voorkeure.</p>

    <h2>5. Kommunikasie</h2>
    <h3>Boodskappe</h3>
    <p>In-app boodskappe tussen lidmate en leiers.</p>
    <h3>Betalings</h3>
    <p>Bekyk betalingsgeskiedenis en doen aanlyn betalings (EFT, Yoco).</p>

    <h2>6. Leer & Groei</h2>
    <h3>Bybelkennis</h3>
    <p>Bybelstudie, verse en geloofsverdieping.</p>
    <h3>Geloofsgroei</h3>
    <p>LMS-kursusse vir geloofsopbou en dissipelskap.</p>
    <h3>Sakramentsbeloftes ("Jy is Myne")</h3>
    <p>Hulpmiddels vir ouers om kinders voor te berei op doop en nagmaal: fases, joernaal, toolkit en aktiviteite.</p>
    <h3>Kort & Kragtig</h3>
    <p>Kinderkerk-lesse vir 6–11 jariges met stories, aktiwiteite en toetse.</p>
    <h3>Geloofsonderrig (KIOG)</h3>
    <p>KI-ondersteunde geloofsonderrig vir hoërskool leerders (12–17) en mentors: AI-chatbot, prentgenerering, KGVW-analise.</p>

    <h2>7. VBO & Bediening</h2>
    <h3>VBO Krediete</h3>
    <p>Voortgesette Bedieningsopleiding: predikante volg 150 krediete oor drie jaar deur kursusse en aktiwiteite.</p>
    <h3>Bedieningsbehoeftes</h3>
    <p>Gemeentes kan bedieningsbehoeftes (bv. preekbeurte) aanmeld; predikante kan reageer en help.</p>
    <h3>Missionale Bediening</h3>
    <p>Stap-vir-stap reis vir missionale gemeenskapsvorming in Afrika: projekte, gebed, en gemeenskap.</p>

    <h2>8. Pastorale & Krisis</h2>
    <h3>Pastorale Aksies</h3>
    <p>Log pastoraatbesoeke, opvolg en aksies. Werks van-lyn-af.</p>
    <h3>Krisisverslae</h3>
    <p>Rapporteer krisisse (siekte, sterftes, noodsituasies) vir vinnige opvolg.</p>

    <h2>9. Gemeenskap & Diens</h2>
    <h3>Advertensies</h3>
    <p>Plaas en bekyk gemeente-advertensies (verkoop, dienste, versoeke).</p>
    <h3>Gawes Soek</h3>
    <p>Soek en koppel lidmate se gawes en talente aan bedienings.</p>
    <h3>Gemeenteprogram</h3>
    <p>Kalender van gemeente-aktiwiteite en gebeurtenisse.</p>
    <h3>Vrae & Versoeke</h3>
    <p>Lidmate kan vrae en versoeke indien; admins kan antwoord.</p>
    <h3>Verhoudings</h3>
    <p>Bestuur verhoudings tussen lidmate en pastoraat.</p>

    <h2>10. Artikels & Redaksie</h2>
    <h3>Artikels-portaal</h3>
    <p>Indien en lees teologiese artikels, redaksionele stukke en kerknuus.</p>
    <h3>Redaksie-portaal</h3>
    <p>Moderators beoordeel en publiseer ingediende artikels.</p>

    <h2>11. Kuberkermis</h2>
    <p>Virtuele kermis vir gemeentes: skep produkte, verkoop aanlyn, bestel kaartjies. Lidmate kan by die publieke kermis-bladsy koop.</p>

    <h2>12. Van-lyn-af & Tegnies</h2>
    <h3>Van-lyn-af Bestuur</h3>
    <p>Bestuur data wat van-lyn-af gestoor is: sinkroniseer, los konflikte op, verwyder kas.</p>
    <h3>Gebruiksaanwysings</h3>
    <p>Tutoriale en hulp vir nuwe gebruikers.</p>
    <h3>Administrasie</h3>
    <p>Gemeente-admins: gebruikers, rolle, instellings, CSV-invoer.</p>

    <h2>Rolle & Toegang</h2>
