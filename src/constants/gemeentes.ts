export type NHKAGemeenteNaam = typeof NHKA_GEMEENTES[number];

// Helper function to sort gemeentes with user's gemeente first
export const sortGemeentesWithUserFirst = <T extends { naam: string }>(
  gemeentes: T[],
  userGemeenteNaam?: string | null
): T[] => {
  if (!userGemeenteNaam) return gemeentes;
  
  return [...gemeentes].sort((a, b) => {
    // User's gemeente always comes first
    if (a.naam === userGemeenteNaam) return -1;
    if (b.naam === userGemeenteNaam) return 1;
    // Otherwise sort alphabetically
    return a.naam.localeCompare(b.naam);
  });
};

// Local storage key for remembering last selected gemeente
export const LAST_GEMEENTE_KEY = 'nhka_last_gemeente';

// Get last selected gemeente from localStorage
export const getLastSelectedGemeente = (): string | null => {
  try {
    return localStorage.getItem(LAST_GEMEENTE_KEY);
  } catch {
    return null;
  }
};

// Save last selected gemeente to localStorage
export const saveLastSelectedGemeente = (gemeenteNaam: string): void => {
  try {
    localStorage.setItem(LAST_GEMEENTE_KEY, gemeenteNaam);
  } catch {
    // Ignore localStorage errors
  }
};
